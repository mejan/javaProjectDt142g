    private void reScale(File a){
        File file1 =a;
        Rectangle rect = picLabelInfol.getBounds();
        image = new ImageIcon(file1.getAbsolutePath());
            Image scaledImage = image.getImage().getScaledInstance(rect.width, rect.height, Image.SCALE_DEFAULT);
            image = new ImageIcon(scaledImage);
            picLabelInfol.setIcon(image);
            
    }
    
    
