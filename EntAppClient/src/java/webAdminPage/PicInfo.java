    public byte[] codeToByte(File f){
                String imgStr;
                String fileName = null;
                byte[] imgbytes = null;
                
                        String imagePath=f.getAbsolutePath();
                try{
                    String imageSRC = (imagePath);
                    File files= new File(imageSRC);
                    fileName=files.getName();
                    BufferedImage img = ImageIO.read(files);
                    String extension= getFileExtension(files);
                    imgStr = encodeToString(img,extension);
                    imgbytes = imgStr.getBytes();
                    
                }catch(IOException e){
                    System.out.println(e);
                }
                return imgbytes;
    }
        
        
